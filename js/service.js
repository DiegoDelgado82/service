function buscarCoincidencias() {
            let inputText = document.getElementById("inputBusqueda").value.toLowerCase();
            let resultados = document.getElementById("resultados");

            
            resultados.innerHTML = "";

           
            let marcas = new Set();

            
            for (let marca in stInfo) {
                if (marca.toLowerCase().includes(inputText)) {
                    marcas.add(marca);
                }
            }

            
            marcas.forEach(function (marca) {
                let listItem = document.createElement("li");
                listItem.textContent = marca;
                listItem.addEventListener("click", function () {
                    mostrarRegistros(marca);
                });
                resultados.appendChild(listItem);
            });
        }

        function mostrarRegistros(marca) {
            let registros = stInfo[marca];
            let detalle = document.getElementById("detalle");
            detalle.innerHTML = "";

            registros.forEach(function (registro) {
                let categoria = registro.CATEGORIA;
                let telefono = registro.TELEFONO;
                let web = registro.WEB;

                let p = document.createElement("p");
                p.innerHTML = "<span id='rojo'>"+marca+"</span>-" + categoria + "<br> Teléfono: " + telefono + "<br>Web: <a href='"+web+"' target='blank'>" + web+"</a>";
		detalle.appendChild(p);	
		/*Crear el botón con clase de boostrap y la función para copiar los datos en el portapapeles*/	
		let btn = document.createElement("button");
            	btn.innerText = "Copiar datos";
		btn.classList.add("button.mb-5");
		let info="Marca: "+marca+"-"+categoria+ "\nTeléfono: " + telefono + "\nWeb: "+web
		btn.addEventListener("click", function(){copiarDatos(info)});
                detalle.appendChild(btn);
		let p2 = document.createElement("p");
                p2.innerHTML = "<br>";
		detalle.appendChild(p2);
            });
        }



	/*Crear la función para copiar los datos en el portapapeles*/	

	function copiarDatos(dato) {
	  var input = document.createElement("input");
	  input.value = dato;
	  document.body.appendChild(input);
	  input.select();
	  document.execCommand("copy");
	  document.body.removeChild(input);
	  alert("¡Se copiaron los datos!");
	}
